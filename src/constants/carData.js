// 제조사 목록
export const MAKERS = CAR_DATA.map(d => d.maker);

// 제조사로 모델 목록 조회
export function getModels(maker) {
  const found = CAR_DATA.find(d => d.maker === maker);
  return found ? found.models : [];
}

// 제조사 + 모델로 연식 목록 조회
export function getYears(maker, modelName) {
  const models = getModels(maker);
  const model = models.find(m => m.name === modelName);
  return model ? [...model.years].sort((a,b) => b-a) : [];
}

// 제조사 + 모델로 연료 목록 조회
export function getFuels(maker, modelName) {
  const models = getModels(maker);
  const model = models.find(m => m.name === modelName);
  return model ? model.fuel : [];
}

// 제조사 + 모델로 오일 스펙 조회 (유료 연동용)
export function getOilSpec(maker, modelName) {
  const models = getModels(maker);
  const model = models.find(m => m.name === modelName);
  return model ? model.oilSpec : null;
}

// 권장 교체 km (연료 타입 기준)
export function getRecommendedKm(fuel) {
  switch(fuel) {
    case "디젤":    return 15000;
    case "가솔린":  return 10000;
    case "LPG":     return 10000;
    case "하이브리드": return 10000;
    default:        return null; // 전기/수소는 엔진오일 불필요
  }
}

// 권장 교체 시간 (엔진부담시간 기준)
export function getRecommendedHour(fuel) {
  switch(fuel) {
    case "디젤":    return 250;
    case "가솔린":  return 200;
    case "LPG":     return 180;
    case "하이브리드": return 200;
    default:        return null;
  }
}
